// ========= Operadores Relacionais ==========

// > -> Maior que 
// < -> Menor que
// >= -> Maior igual a 
// <= -> Menor igual a

// ======== Operadores de Igualdade =========

// == -> Igual a 
// != -> Diferente de 
// === -> Igual e do mesmo tipo
// !== -> Diferente inclusive do tipo

// ========= Operadores Logicos ==========

// && -> Retorna verdadeiro quando as duas expressoes sao verdadeiras
// || -> Retorna verdadeiro quando umas das expressoes sao verdadeiras
// !  -> Vai negar uma expressao


// ========== Operadores Aritmeticos ===========

// * -> Multiplicao
// / -> Divisao
// + -> Adicao
// - -> Subtracao
// % -> Resto da Divisao